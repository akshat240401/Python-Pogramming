#Sorting
l1 = [1,8,7,2,21,15]
l1.sort()
print(l1)

#Reversing
l1.reverse()
print(l1)

#Append
l1.append(45) #Appends at end
print(l1)

#Insert
l1.insert(0,500) #Inserts at position i.e. (position,insert vlaue)
print(l1)

#Pop
l1.pop()
print(l1)

#Remove
l1.remove(21) #Removes a particular element
print(l1)